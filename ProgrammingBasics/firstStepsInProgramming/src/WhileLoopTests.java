public class WhileLoopTests {
    public static void main(String[] args) {
        int count = 1;

        // Използване на while цикъл за извеждане на числата от 1 до 5
        while (count <= 5) {
            System.out.printf("Число: %d%n", count);
            count++; // Увеличаване на брояча
        }

        System.out.println("Цикълът приключи.");
    }
}
